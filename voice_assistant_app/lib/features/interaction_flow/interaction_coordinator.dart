extension LanguageCodeMapping on InteractionCoordinator {
  /// Returns the correct language code for speech recognition (STT)
  String get sttLanguage {
    switch (selectedLanguage) {
      case "ta_IN":
        return "ta_IN"; // Tamil for STT
      case "ml_IN":
